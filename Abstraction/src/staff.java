
public abstract class staff {

	public abstract void calculateSalary();
	public abstract void qualification();
	public abstract void Department();
	
	private String name;
	
	public staff() {
		
	}
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	

	
	

}
